    private void btnGenerateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateActionPerformed
        String code = txtCode.getText();
        String name = txtName.getText();
        String lev = txtLevel.getText();
        String cr = txtCreator.getText();
        String crd = txtCreated.getText();
        String call = txtCall.getText();
        String sect = txtSection.getText();
        String det = txtDetails.getText();
        String call1 = txtCall1.getText();
        String res1 = txtResult1.getText();
        String comm1 = txtComment1.getText();
        String call2 = txtCall2.getText();
        String res2 = txtResult2.getText();
        String comm2 = txtComment2.getText();
        String call3 = txtCall3.getText();
        String res3 = txtResult3.getText();
        String comm3 = txtComment3.getText();
        String programCode = jTextAreaCode.getText();
        ArrayList<Inputs_Outputs> inp = getInputs();
        ArrayList<Inputs_Outputs> out = getOutputs();
        
        Json json = new Json(code,name,lev,cr,crd,call,sect,det,call1,res1,comm1,call2,res2,comm2,call3,res3,comm3,programCode,inp,out);
        
        if(json.generateJson()){
            JOptionPane.showMessageDialog(null, "Json created");
        }
        else{
            JOptionPane.showMessageDialog(null, "There was an error!");
        }
        clearAll();

    
    }//GEN-LAST:event_btnGenerateActionPerformed

