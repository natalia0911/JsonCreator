    public ArrayList<Inputs_Outputs> getInputs(){
        int rowCount = inputs.getRowCount();
        ArrayList<Inputs_Outputs> inputsList =  new ArrayList();
        for(int i=0;i<rowCount;i++){
            Inputs_Outputs in = new Inputs_Outputs();
            in.setName(String.valueOf(inputs.getValueAt(i, 0)));
            in.setType(String.valueOf(inputs.getValueAt(i, 1)));
            inputsList.add(in);
        }
        return inputsList;
    }
    
    public ArrayList<Inputs_Outputs> getOutputs(){
       int rowCount = outputs.getRowCount();
        ArrayList<Inputs_Outputs> outputsList =  new ArrayList();
        for(int i=0;i<rowCount;i++){
            Inputs_Outputs out = new Inputs_Outputs();
            out.setName(String.valueOf(outputs.getValueAt(i, 0)));
            out.setType(String.valueOf(outputs.getValueAt(i, 1)));
            outputsList.add(out);
        }
        return outputsList;
    }
        
